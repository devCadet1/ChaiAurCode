// function expression (normal function)
function add(n,m) {
    return n + m
}

console.log(add(2,3))   // 5


// Arrow Function  () => {}

const addition = (a , b) => {
    console.log( a + b    )
}

addition(4,4)   // 8

// more ways to write a ArrowFunc

const square = (num) => num*num

console.log(square(4))
